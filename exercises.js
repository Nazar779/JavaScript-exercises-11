// write a function that converts minutes to seconds
function convertMinutesInSeconds(minutes){
    return 60 * parseInt(minutes)
}
console.log(convertMinutesInSeconds(2))
// write function that find area of triangle formula area of triangle (base / height) / 2
function AreaOfTriangle(base, height){
    return(base * height) / 2
}
console.log(AreaOfTriangle(10, 6))
// create function that convert age to  seconds ignore leap year
function convertAgetoDays(age){
    return 365 * age
}
